export const add = function(a, b) {
    let sum = a + b
    return sum;
};

export const subtract = function(a, b) {
    let total = a - b;
    return total;
};

export const divide = function(a, b) {
    let total = a / b;
    return total;
};

export const multiply = function(a, b) {
    let total = a * b;
    return total;
};